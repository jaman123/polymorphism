package polymorphism;

public class LandCalculator {
	public double areaOfLand(double a, double b) {
		double total= a + b; 
		return total;
	}
	public double areaOfLand(double a, double b, double c) {
		double total= a + b + c; 
		return total;
	}
	public double areaOfLand(double a, double b, double c, double d) {
		double total= a + b + c + d; 
		return total;
	}

}
